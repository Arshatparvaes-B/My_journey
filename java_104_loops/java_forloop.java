package java_104_loops;

public class java_forloop {
    public static void main(String args[]){
        for (int counter=0;counter<100;counter++){
            System.out.println("Hi from java "+counter+" times"+" ");
        }
        System.out.println("Remember if you don't give the comndition for the loop to end then it will go on forever(till the memory limit is reached.)");
        System.out.println("The counter starts from 0.and ends at n-1");
    }
    
}
